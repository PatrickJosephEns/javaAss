package npackage;

public class dhb {

	private String dhb;
	private int active;
	private int recovered;
	private int deceased;
	private int total;
	private int lastDay;
	
	public dhb(String dhb, int active, int recovered, int deceased, int total, int lastDay) {
		super();
		this.dhb = dhb;
		this.active = active;
		this.recovered = recovered;
		this.deceased = deceased;
		this.total = total;
		this.lastDay = lastDay;
	}
	
	
}
